import java.awt.geom.*;

/**
 * Pi³ka, która porusza siê i odbija od krawêdzi prostok¹ta
 * 
 * @version 1.33 2007-05-17
 * @author Cay Horstmann
 */
public class Ball {
	private static final int XSIZE = 35;
	private static final int YSIZE = 350;
	private double x = 0;
	private double y = 0;
	private double dx = 1;
	private double dy = 1;

	/**
	 * Przesuwa pi³kê do nastêpnego po³o¿enia, odwracaj¹c kierunek, jeœli pi³ka
	 * uderzy w krawêdŸ
	 */

	public void move(Rectangle2D bounds) {
		x += dx;
		y += dy;
		
		if (x < bounds.getMinX()) {
			x = bounds.getMinX();
			dx = -dx;
			
		}
		if (x + XSIZE >= bounds.getMaxX()) {
			x = bounds.getMaxX() - XSIZE;
			dx = -dx;
		}
		if (y < bounds.getMinY()) {
			y = bounds.getMinY();
			dy = -dy;
		}
		if (y + YSIZE >= bounds.getMaxY()) {
			y = bounds.getMaxY() - YSIZE;
			dy = -dy;
		}
	}


	public Ellipse2D getShape(){
		return new Ellipse2D.Double(x, y, XSIZE, YSIZE);
		
	}
}